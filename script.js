function searchResults() {
    const examNumberInput = document.getElementById('examNumber').value.trim();
    const resultContainer = document.getElementById('result');
    const resultTable = document.getElementById('resultTable');
    const errorMessage = document.getElementById('error-message');
    const searchIcon = document.getElementById('search-icon');
    const searching = document.getElementById('searching');

    resultContainer.classList.add('hidden');
    errorMessage.classList.add('hidden');
    searchIcon.classList.add('hidden');
    searching.classList.remove('hidden');

    setTimeout(() => {
        searchIcon.classList.remove('hidden');
        searching.classList.add('hidden');

        const student = data.find(item => item.examNumber === examNumberInput);
        
        if (student) {
            resultTable.innerHTML = `
                <tr>
                    <th>Exam Number</th>
                    <th>Name</th>
                    <th>Gender</th>
                    <th>Class</th>
                </tr>
                <tr>
                    <td>${student.examNumber}</td>
                    <td>${student.firstName} ${student.middleName} ${student.lastName}</td>
                    <td>${student.gender === 'M' ? 'Male' : 'Female'}</td>
                    <td>${student.class}</td>
                </tr>
            `;
            resultContainer.classList.remove('hidden');
        } else {
            errorMessage.textContent = 'Student not found or invalid exam number';
            errorMessage.classList.remove('hidden');
        }
    }, 1000);
}

